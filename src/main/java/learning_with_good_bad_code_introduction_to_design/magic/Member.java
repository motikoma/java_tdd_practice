package learning_with_good_bad_code_introduction_to_design.magic;

public class Member {
    final int level;
    final int agility;
    final int magicAttack;
    final int vitality;

    public Member(final int level, final int agility, final int magicAttack, final int vitality) {
        this.level = level;
        this.agility = agility;
        this.magicAttack = magicAttack;
        this.vitality = vitality;
    }
}
